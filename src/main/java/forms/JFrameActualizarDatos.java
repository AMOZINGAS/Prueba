    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        
        if(txtNombres.getText().equalsIgnoreCase("") || txtApellidoPaterno.getText().equalsIgnoreCase("") || txtApellidoMaterno.getText().equalsIgnoreCase("")
                || txtCalle.getText().equalsIgnoreCase("") || txtColonia.getText().equalsIgnoreCase("") || txtCodigoPostal.getText().equalsIgnoreCase("") ){
            
            JOptionPane.showMessageDialog(this, "Porfavor, no deje campos vacios", "ERROR!!", JOptionPane.ERROR_MESSAGE );
            
        }else{
        
            clienteDTO.setNombres(txtNombres.getText());
            clienteDTO.setApellidoPaterno(txtApellidoPaterno.getText());
            clienteDTO.setApellidoMaterno(txtApellidoMaterno.getText());
            clienteDTO.setCalle(txtCalle.getText());
            clienteDTO.setColonia(txtColonia.getText());
            clienteDTO.setCodigoPostal(txtCodigoPostal.getText());
            IConexionBD conexionBD = new ConexionBD();
            IClienteDAO clienteDAO = new ClienteDAO(conexionBD);
            ClienteDTO clienteValidado = new ClienteDTO();
            clienteValidado = clienteDAO.editar(clienteDTO);
            if(clienteValidado == null){
                
                JOptionPane.showMessageDialog(this, "No se pudo actualizar al cliente", "ERROR!!", JOptionPane.ERROR_MESSAGE);
                
            }else{
                
                JOptionPane.showMessageDialog(this, "Se actualiz√≥ al cliente exitosamente", "EXITO!!", JOptionPane.INFORMATION_MESSAGE);
                JFrameSeleccionCuenta cuentas = new JFrameSeleccionCuenta(conexionBD, clienteDTO);
                cuentas.setVisible(true);
                this.dispose();
                
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

